    private void anioTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_anioTextKeyTyped
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c) || c == java.awt.event.KeyEvent.VK_BACK_SPACE || c == java.awt.event.KeyEvent.VK_DELETE)) {
            evt.consume(); // Ignora la tecla
        }
    }//GEN-LAST:event_anioTextKeyTyped

    private void cargaTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cargaTextKeyTyped
        char c = evt.getKeyChar();
        // Solo permite números y el punto
        if (!(Character.isDigit(c) || c == '.' || c == java.awt.event.KeyEvent.VK_BACK_SPACE)) {
            evt.consume();
        }
        // Evita que pongan más de un punto
        if (c == '.' && ((JTextField)evt.getSource()).getText().contains(".")) {
            evt.consume();
        }
    }//GEN-LAST:event_cargaTextKeyTyped

