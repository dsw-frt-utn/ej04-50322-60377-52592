    public void agregarVehiculoView(){
        String nombre = this.marcaText.getText();
        String pais = this.paisText.getText();
        String patente = this.patenteText.getText();
        String modelo = this.modeloText.getText();
        int anio =  Integer.parseInt(this.anioText.getText());
        double capacidad = Double.parseDouble(this.cargaText.getText());
        double kmLitro = Double.parseDouble(this.kmLitroText.getText());
        double litrosExtra = Double.parseDouble(this.litrosExtraText.getText());
        
        boolean result = controlador.altaVehiculo(nombre, pais, patente, modelo, anio, capacidad, kmLitro, litrosExtra);
        
        if(result == true){
            JOptionPane.showMessageDialog(this, "¡Vehiculo ingresado con éxito!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            this.padre.listarVehiculos();
            this.salirButton.doClick();
        }
        else{
            JOptionPane.showMessageDialog(this, "Error al crear el vehiculo, verifique los campos", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }
        
        }
